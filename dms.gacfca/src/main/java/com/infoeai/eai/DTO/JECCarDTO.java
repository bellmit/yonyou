package com.infoeai.eai.DTO;

public class JECCarDTO {
	
	private String buyTime; // 购车时间
	private String buyProvince; // 购车省/直辖市
	private String buyCity; // 购车市/区
	private String trunkCode; // 车架号
	private String buyDealer; // 购车经销商
	private String model; // 购买车型
	private String style; // 车款
	private String color; // 颜色
	private String motorCode; // 发动机编码
	private String carCode; // 牌照号
	private String isNewCar; // 新车/二手车
	private String deliverMileage; // 交付里程表数
	private String buyStatus; // 购车状态
	
	public String getBuyTime() {
		return buyTime;
	}
	public void setBuyTime(String buyTime) {
		this.buyTime = buyTime;
	}
	public String getBuyProvince() {
		return buyProvince;
	}
	public void setBuyProvince(String buyProvince) {
		this.buyProvince = buyProvince;
	}
	public String getBuyCity() {
		return buyCity;
	}
	public void setBuyCity(String buyCity) {
		this.buyCity = buyCity;
	}
	public String getTrunkCode() {
		return trunkCode;
	}
	public void setTrunkCode(String trunkCode) {
		this.trunkCode = trunkCode;
	}
	public String getBuyDealer() {
		return buyDealer;
	}
	public void setBuyDealer(String buyDealer) {
		this.buyDealer = buyDealer;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getMotorCode() {
		return motorCode;
	}
	public void setMotorCode(String motorCode) {
		this.motorCode = motorCode;
	}
	public String getCarCode() {
		return carCode;
	}
	public void setCarCode(String carCode) {
		this.carCode = carCode;
	}
	public String getIsNewCar() {
		return isNewCar;
	}
	public void setIsNewCar(String isNewCar) {
		this.isNewCar = isNewCar;
	}
	public String getDeliverMileage() {
		return deliverMileage;
	}
	public void setDeliverMileage(String deliverMileage) {
		this.deliverMileage = deliverMileage;
	}
	public String getBuyStatus() {
		return buyStatus;
	}
	public void setBuyStatus(String buyStatus) {
		this.buyStatus = buyStatus;
	}
	
	

}
